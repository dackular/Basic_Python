def print_hello_world(no_of_time):
    for i in range(1,no_of_time+1):
        print("Hello World")


print_hello_world(3)


def print_number(n):
    for i in range(1,n+1):
        print(i)

print_number(3)


def print_square_number(z):
    for y in range(1,z+1):
        print(y*y)

print_square_number(5)